/**
 * JS Loops -- allows us to repeat a set of instructions multiple times.
 */

// for loop
for (let x = 0; x < 10; x++) {
    console.log(x);
    console.log("Current value of x: ", x);
}

// while loop
let count = 0;
while (count < 10) {
    count++;
    console.log(count);
}

let myNum = 1;
while (myNum < 5) {
    console.log(myNum);
    myNum++;
}


